def dt_convert(date_str):
    """Takes a date string (non-`datetime` object) and
    converts it to a `datetime` object string."""
    format = "%b %d, %Y"
    dt_obj = dt.strptime(date_str, format)
    dt_str = dt_obj.strftime("%x")
    return dt_str


